const dictionaries: Record<Exclude<LocaleCode, "system">, Dictionary> = {
  en,
  "zh-CN": zhCN,
  "zh-TW": zhTW,
};

let currentLocale: LocaleCode = "system";

export function setLocale(locale: LocaleCode | undefined): void {
  currentLocale = locale || "system";
}

export function getLocale(): LocaleCode {
  return currentLocale;
}

export function getEffectiveLocale(locale: LocaleCode = currentLocale): Exclude<LocaleCode, "system"> {
  if (locale !== "system") return locale;
  const lang = (document.documentElement.lang || navigator.language || "").toLowerCase();
  if (lang.includes("zh-tw") || lang.includes("zh-hk") || lang.includes("zh-hant")) return "zh-TW";
  if (lang.startsWith("zh")) return "zh-CN";
  return "en";
}

export function t(key: string, vars?: Record<string, string | number>): string {
  const locale = getEffectiveLocale();
  const template = dictionaries[locale][key] || dictionaries.en[key] || key;
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ""));
}
